# -------------------------------
# Завдання 5: Арифметичні операції з двома змінними
# -------------------------------
a = float(input("Введіть a: "))
b = float(input("Введіть b: "))

print("Сума:", a + b)
print("Різниця:", a - b)
print("Множення:", a * b)
print("Ділення:", a / b if b != 0 else "Ділення на нуль неможливе")
print("a == b:", a == b)
print("a > b:", a > b)
print("a < b:", a < b)

